void main() {
  // > < >= <= !=
  // If
  var num = 6;
  if (num == 5) {
    print("This number is 5");
  }

  // If/Else
  if (num == 5) {
    print("This number is 5");
  } else {
    print("This number is not 5");
  }

  // If Else if
  if (num == 5) {
    print("This number is 5");
  } else if (num == 6) {
    print("This number is 6");
  }
}
